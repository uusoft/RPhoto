package com.raysmond.bean;

public class Role {
	private int rid;
	private String name;
	
	public Role(){
		
	}

	public int getRid() {
		return rid;
	}

	public void setRid(int rid) {
		this.rid = rid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
